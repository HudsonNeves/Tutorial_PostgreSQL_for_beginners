-- LINGUAGEM DE CONSULTA ESTRUTURADA - SQL
-- Padronização SQL
-- Subconjuntos da Linguagem SQL
/*
	DML - Linguagem de manipulação de dados
	UPDATE - usado para alterar dados
	SELECT - Consulta de dados
	DELETE - excluir registros
	INSERT - usado para inserir dados
*/
/*
	DDL - Linguagem de definição de dados
	ALTER - permite alterar um objeto do banco
	CREATE - permite criar objetos
	DROP - permite apagar um objeto do banco	
*/
/*
	DCL - Linguagem de controle de dados
	GRANT - concede privilégios
	REVOKE - remove privilégios
*/
/*
	DTL - Linguagem de transação de dados
	BEGIN - marca um inicio da transação
	COMMIT - encerra uma transação
	ROLLBACK - encerra a transação, desfazendo todas as operação
*/
/*
	DQL - Linguagem de consulta de dados
	SELECT - consulta
*/